		pomysłu kadry WWW - programu tutorialowego. Oferujemy uczestnikom
		stałą (tj. rozciągającą się także poza Warsztaty) opiekę naukową ze
		strony kadry WWW. Każdy chętny dostanie swojego "tutora", którego
		zadaniem będzie pomoc w szeroko rozumianym rozwoju naukowym -
		polecanie lektur, dyskusje o przeczytanych książkach i ciekawiących
		podopiecznego problemach oraz pomoc w szukaniu odpowiedzi, sugestie,
		czym warto się zainteresować etc. Co jakiś czas (np. co 2 tygodnie
		albo miesiąc) podopieczny pisze, czego się w tym czasie nauczył, co
		przeczytał, nad jakimi zadaniami i problemami się zastanawiał itd.
		Szczegóły współpracy do ustalenia z tutorem. Projekt jest na razie w
		fazie beta-testów - jeśli okaże się udany, rozreklamujemy go szerzej.</p>

